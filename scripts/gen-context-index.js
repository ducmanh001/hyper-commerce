#!/usr/bin/env node
/**
 * gen-context-index.js
 *
 * Auto-generates the navigation sections of SCHEMA.md from live code:
 *   - "Next migration number" (from infrastructure/postgres/migrations/)
 *   - "Table → Service Map" (from @Entity decorators in entity files)
 *
 * Run: node scripts/gen-context-index.js
 *      make context:index
 *
 * DOES NOT touch: business rules section, EVENTS.md (routing is manually maintained).
 * Safe to run multiple times — idempotent.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const SCHEMA_PATH = path.join(ROOT, 'infrastructure/postgres/SCHEMA.md');

// ── 1. Next migration number ──────────────────────────────────────────────────

function getNextMigrationNumber() {
  const migrationsDir = path.join(ROOT, 'infrastructure/postgres/migrations');
  if (!fs.existsSync(migrationsDir)) return 1;

  const files = fs.readdirSync(migrationsDir)
    .filter(f => f.endsWith('.sql') || f.endsWith('.ts'))
    .map(f => {
      const match = f.match(/^(\d+)/);
      return match ? parseInt(match[1], 10) : 0;
    })
    .filter(n => n > 0)
    .sort((a, b) => a - b);

  return files.length > 0 ? files[files.length - 1] + 1 : 1;
}

// ── 2. Table map from @Entity decorators ─────────────────────────────────────

function findEntityFiles(dir, results = []) {
  if (!fs.existsSync(dir)) return results;
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      findEntityFiles(full, results);
    } else if (entry.isFile() && entry.name.endsWith('.entity.ts')) {
      results.push(full);
    }
  }
  return results;
}

function parseEntityFiles() {
  const appsDir = path.join(ROOT, 'apps');
  const entityFiles = findEntityFiles(appsDir);
  const rows = [];

  for (const filePath of entityFiles) {
    const content = fs.readFileSync(filePath, 'utf8');
    const entityMatch = content.match(/@Entity\(['"]([^'"]+)['"]\)/);
    if (!entityMatch) continue;

    const tableName = entityMatch[1];
    const relPath = path.relative(ROOT, filePath);

    // Extract service name from path: apps/{service-name}/src/...
    const serviceMatch = relPath.match(/^apps\/([^/]+)\//);
    const serviceName = serviceMatch ? serviceMatch[1] : 'unknown';

    rows.push({ tableName, serviceName, relPath });
  }

  // Sort by service, then table name
  rows.sort((a, b) => {
    if (a.serviceName !== b.serviceName) return a.serviceName.localeCompare(b.serviceName);
    return a.tableName.localeCompare(b.tableName);
  });

  return rows;
}

// ── 3. Build new SCHEMA.md ────────────────────────────────────────────────────

function buildTableMapSection(rows) {
  const lines = [
    '## Table → Service Map',
    '',
    '> Auto-generated by `make context:index` — do NOT edit manually.',
    '',
    '| Table | Service | Entity file |',
    '|-------|---------|-------------|',
  ];

  for (const { tableName, serviceName, relPath } of rows) {
    lines.push(`| \`${tableName}\` | ${serviceName} | \`${relPath}\` |`);
  }

  return lines.join('\n');
}

function updateSchemaFile(nextMigration, tableRows) {
  let content = fs.readFileSync(SCHEMA_PATH, 'utf8');

  // Replace "Next migration number: NNN"
  content = content.replace(
    /## Next migration number:\s*\*\*\d+\*\*/,
    `## Next migration number: **${nextMigration}**`,
  );

  // Replace planned sequence comment (optional, just update the first number)
  content = content.replace(
    /Planned sequence:.*\n/,
    `Planned sequence: ${nextMigration}=next · auto-update via \`make context:index\`\n`,
  );

  // Replace table map section — find start marker to end of table (before next ##)
  const tableMapRegex = /## Table → Service Map\n[\s\S]*?(?=\n---|\n## [A-Z]|$)/;
  const newSection = buildTableMapSection(tableRows);
  if (tableMapRegex.test(content)) {
    content = content.replace(tableMapRegex, newSection + '\n');
  } else {
    // Section not found — append before business rules
    const bizRulesIdx = content.indexOf('## Business Rules');
    if (bizRulesIdx !== -1) {
      content = content.slice(0, bizRulesIdx) + newSection + '\n\n---\n\n' + content.slice(bizRulesIdx);
    } else {
      content += '\n\n' + newSection + '\n';
    }
  }

  fs.writeFileSync(SCHEMA_PATH, content, 'utf8');
}

// ── 4. Main ───────────────────────────────────────────────────────────────────

function main() {
  console.log('📐 gen-context-index — scanning codebase...\n');

  const nextMigration = getNextMigrationNumber();
  console.log(`  Migration files: next number = ${nextMigration}`);

  const tableRows = parseEntityFiles();
  console.log(`  Entity files found: ${tableRows.length} tables`);

  tableRows.forEach(r => console.log(`    ${r.tableName.padEnd(30)} ← ${r.relPath}`));

  updateSchemaFile(nextMigration, tableRows);
  console.log(`\n✅ SCHEMA.md updated (${tableRows.length} tables, next migration: ${nextMigration})`);
  console.log('   Business rules section preserved.\n');
  console.log('NOTE: EVENTS.md routing is manually maintained (topic→service mapping');
  console.log('      is not inferrable from code alone — update after adding topics).');
}

main();
